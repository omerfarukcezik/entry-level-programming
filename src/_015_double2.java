public class _015_double2 {
    public static void main(String[] args) {
        int x, y, z;

        x = 15;
        System.out.println("x=" + x);

        y = 5;
        System.out.println("y=" + y);

        z = x / y;
        System.out.println("x/y=" + z);

        z = x % y;
        System.out.println("x%y=" + z);
    }
}
